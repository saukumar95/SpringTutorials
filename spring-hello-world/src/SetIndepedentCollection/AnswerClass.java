package SetIndepedentCollection;


public class AnswerClass 
{
  private String answer;
  private String by;

  
    public void setAnswer(String answer) {
        this.answer = answer;
    }

    public void setBy(String by) {
        this.by = by;
    }

    @Override
    public String toString() {
        return "AnswerClass{" + "answer=" + answer + ", by=" + by + '}';
    }
  
  
    
}
